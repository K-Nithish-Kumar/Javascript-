// Mathematical & Arithmetic operators -> + , - , * , /
let num1 = 10;
let num2 = 20;

console.log(num1 + num2);
console.log(num1 - num2);
console.log(num1 * num2);
console.log(num1 / num2);
console.log(num1 ** num2);

// Assignmet operator
let x, y;
console.log(x = y = 10 + 28 + 30);

// Comparision Operator -> (>, <, >=, <=)
console.log(num1 > num2);
console.log(num1 >= num2);
console.log(num1 < num2);
console.log(num1 <= num2);
